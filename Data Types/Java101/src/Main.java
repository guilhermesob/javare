public class Main {
    public static void main(String[] args) {
       // Data Types in Java
        int newTemp = 30;
        float newFloatValue = 10.75f;
        boolean isCorrect = true;
        char newLetter = 'E';
        String newMessage = "Java is powerful";
        // Other types
        byte newVal = 120;
        short newVal2 = 30000;
        long newLongVal = 3000000000000000L;
        double newDecimalVal = 39.87d;

        // Display values on screen
        System.out.println(newTemp);
        System.out.println(newFloatValue);
        System.out.println(isCorrect);
        System.out.println(newLetter);
        System.out.println(newMessage);
        System.out.println(newVal);
        System.out.println(newVal2);
        System.out.println(newLongVal);
        System.out.println(newDecimalVal);
    }
}
